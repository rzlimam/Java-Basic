package redzone;

public class LevelKamar {
	
	
	private String levelKamar;
	private int harga;
	
	public LevelKamar(String levelKamar, int harga) {
		super();
		this.levelKamar = levelKamar;
		this.harga = harga;
	}

	public String getLevelKamar() {
		return levelKamar;
	}

	public void setLevelKamar(String levelKamar) {
		this.levelKamar = levelKamar;
	}

	public int getHarga() {
		return harga;
	}

	public void setHarga(int harga) {
		this.harga = harga;
	}
	
	
	
	

}
